#include <unistd.h>

void	ft_putnbr(int nb)
{
	int	n;
	int	i;

	n = nb;
	i = n / 10;
	i += 48;
	write(1, &i, 1);
	i = n % 10;
	i += 48;
	write (1, &i, 1);
}

int	main(void)
{
	ft_putnbr(42);
	write(1, "\n", 1);
	return (0);
}
